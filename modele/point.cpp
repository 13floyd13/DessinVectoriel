//-------------------------------------------
Point::Point()
//-------------------------------------------
{
    m_x = m_y = 0;
    m_pointCount++;
}

//-------------------------------------------
Point::Point(int x, int y)
//-------------------------------------------
{
            m_x = x;
    m_y = y;
    m_pointCount++;
}

//-------------------------------------------
Point::Point(int z)
//-------------------------------------------
{
    m_x = m_y = z;
    m_pointCount++;
}

//-------------------------------------------
Point::~Point()
//-------------------------------------------
{
    m_pointCount--;
}

//-------------------------------------------
void Point::Move(int dX, int dY)
//-------------------------------------------
{
    m_x += dX;
    m_y += dY;
}

