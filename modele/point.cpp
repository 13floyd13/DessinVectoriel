//-------------------------------------------
float Point::Distance(const Point *p) const
//-------------------------------------------

{
    return sqrt((m_x - p->m_x) * (m_x - p->m_x) + (m_y - p->m_y) * (m_y - p->m_y));
}

//-------------------------------------------
float Point::Distance(const Point &p) const

//-------------------------------------------
{
    return sqrt((m_x - p.m_x) * (m_x - p.m_x) + (m_y - p.m_y) * (m_y - p.m_y));
}


